# Declara as variáveis x e y
x = 10
y = 20

println("x = ", x)
println("y = ", y)

# Soma Binária
soma = x + y
println("Soma: ", soma)

# Subtração Binária
subt = x-y
println("Subtracao: ", subt)

# Multiplicação
mult = x*y
println("Multiplicacao: ", mult)

# Divisão
divisao = x/y
println("Divisao: ", divisao)

# Divisão Inversa
divInv = x\y
println("Divisao Inversa: ", divInv)

# Potenciação
pot = x^y
println("Potenciacao: ", pot)

# Módulo
resto = x%y
println("Resto: ", resto)
